public class switch_statement_ex3 {
    public static void main(String[] args) {
        Integer x = 200;
        // switch case without break
        switch(x){
            case 10:
                System.out.println("=10");
                
            case 20:
                System.out.println("=20");
                
            case 30:
                System.out.println("=30");
                
            default:
                System.out.println("none of the avobe");
                

        }

    }
}
